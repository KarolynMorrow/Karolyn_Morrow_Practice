package Arrays.demo;

public class ArrayDemoTwo {
    public static void main(String[] args) {
        int[] age = {12, 4, 5, 4}; //making an array named age
        for (int i = 0; i < age.length; i++){ //starting at index 0, as long as index is less than the length of the age array keep going
            System.out.println(age[i]); //print the index
        }
    }
}
