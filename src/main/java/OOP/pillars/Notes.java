package OOP.pillars;

public class Notes {

    /*
    * Encapsulation - The process of wrapping code and data together into a single unit (data/information hiding)
    * Abstraction - The process of hiding implementation details and exposing only the functionality to the user. Methods can be concrete or abstract
    * Inheritance - The process of one class inheriting properties
    * Polymorphism - Only when child class methods are overriding parent class methods
    * Interface - Every class method is an abstract method
    * explicit type casting (Student student1 = (Student) person1;) Highly frowned upon due to possibility of compile error as well as confusion.
    *
    *

    ***                  --- When to choose Abstraction vs Interface ---
    - Abstract classes can have constructors, while interfaces cannot.
    - An abstract class can have instance variables and concrete methods, while an interface cannot have either.
    - A class can extend only one abstract class, but it can implement multiple interfaces.
    - Abstract classes are used to create a hierarchy of related classes, while interfaces are used to define a contract for unrelated classes.
    ***   Are there any common implementation that every subclass should inherit: If so use Abstract if not use Interface

    * Final classes cannot be extended *public final class ClassName(){}* Examples are wrapper classes (Integer class) and String class
    * Final methods cannot be overridden *public final [returnType] [methodName] [
    * Static classes are automatically final
    *
    *
    *
    * */
}
